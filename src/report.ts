function coverageItems(result: ReviewResult): Array<{ label: string; found: boolean; detail?: string }> {
  const signals = result.evidence.signals;
  if (!signals) {
    return [{ label: "Public detail extraction", found: false, detail: "Not available for this review" }];
  }
  const contactFormsFound = countContactForms(result);
  const sitemap = result.evidence.sitemap;
  const registration = result.evidence.domainRegistration;
  const external = result.evidence.externalEvidence;
  const homepageLastModified = result.evidence.pages[0]?.lastModified;

  const pageDetail = (key: keyof typeof signals.pagePresence) => signals.pagePresenceDetails[key];
  const hasLinkedOrReviewedPage = (key: keyof typeof signals.pagePresence) => {
    const detail = pageDetail(key);
    return Boolean(detail?.sources.includes("linked") || detail?.sources.includes("reviewed_page"));
  };
  const pageDetailText = (key: keyof typeof signals.pagePresence) => {
    const detail = pageDetail(key);
    if (!detail) {
      return undefined;
    }
    return detail.urls.slice(0, 2).join(" | ") || detail.sources.map((source) => source.replace(/_/g, " ")).join(", ");
  };

  return [
    { label: "Contact page link", found: hasLinkedOrReviewedPage("contact"), detail: pageDetailText("contact") },
    { label: "Privacy Policy link", found: hasLinkedOrReviewedPage("privacy"), detail: pageDetailText("privacy") },
    { label: "Terms page link", found: hasLinkedOrReviewedPage("terms"), detail: pageDetailText("terms") },
    { label: "About page link", found: hasLinkedOrReviewedPage("about"), detail: pageDetailText("about") },
    { label: "Refund or return page link", found: hasLinkedOrReviewedPage("refund"), detail: pageDetailText("refund") },
    { label: "Shipping or delivery page link", found: hasLinkedOrReviewedPage("shipping"), detail: pageDetailText("shipping") },
    { label: "Cancellation page link", found: hasLinkedOrReviewedPage("cancellation"), detail: pageDetailText("cancellation") },
    { label: "Email address", found: signals.emails.length > 0, detail: signals.emails.slice(0, 2).join(", ") },
    { label: "Phone number", found: signals.phones.length > 0, detail: signals.phones.slice(0, 2).join(", ") },
    {
      label: "Address-like line",
      found: signals.addressLines.length > 0,
      detail: signals.addressLines.slice(0, 1).join(", ")
    },
    {
      label: "Social profile link",
      found: signals.socialLinks.length > 0,
      detail: signals.socialLinks.slice(0, 2).map((link) => `${link.label}: ${link.url}`).join(" | ")
    },
    {
      label: "Contact form",
      found: contactFormsFound > 0,
      detail: contactFormsFound > 0 ? `${contactFormsFound} found` : undefined
    },
    {
      label: "Sitemap",
      found: Boolean(sitemap?.found),
      detail: sitemap ? `${sitemap.status ? `HTTP ${sitemap.status}` : "not available"}; ${sitemap.pageUrls.length} page URL${sitemap.pageUrls.length === 1 ? "" : "s"}` : undefined
    },
    {
      label: "Domain registration dates",
      found: registration?.status === "found" && Boolean(registration.creationDate),
      detail: registration
        ? registration.creationDate
          ? `Created ${registration.creationDate}${registration.lastChangedDate ? `; changed ${registration.lastChangedDate}` : ""}`
          : formatDomainRegistrationStatus(result)
        : undefined
    },
    {
      label: "Homepage last modified",
      found: Boolean(homepageLastModified),
      detail: homepageLastModified ? formatPageLastModified(homepageLastModified) : undefined
    },
    {
      label: "Manual business registration state",
      found: Boolean(result.evidence.input.expectedState),
      detail: result.evidence.input.expectedState
    },
    {
      label: "State registry follow-up link",
      found: Boolean(external?.registryChecks.length),
      detail: external?.registryChecks.map((registry) => `${registry.state}: ${registry.searchUrl}`).join(" | ")
    },
    {
      label: "External public search links",
      found: Boolean(external?.searchLinks.length),
      detail: external ? `${external.searchLinks.length} generated` : undefined
    },
    {
      label: "Website-discovered external profiles",
      found: Boolean(external?.profileLinks.length),
      detail: external?.profileLinks.slice(0, 2).map((profile) => `${profile.platform}: ${profile.url}`).join(" | ")
    }
  ];
}

function renderSignalsMarkdown(result: ReviewResult): string[] {
  const signals = result.evidence.signals;
  if (!signals) {
    return ["- Public detail extraction was not available for this review."];
  }

  const commonPages = (Object.entries(signals.pagePresence) as Array<[keyof typeof signals.pagePresence, boolean]>)
    .filter(([, present]) => present)
    .map(([name]) => name);

  return [
    `- Emails: ${signals.emails.length ? signals.emails.join(", ") : "none found"}`,
    `- Phones: ${signals.phones.length ? signals.phones.join(", ") : "none found"}`,
    `- Contact forms: ${countContactForms(result)}`,
    `- Address-like lines: ${signals.addressLines.length ? signals.addressLines.join(" | ") : "none found"}`,
    `- Common pages: ${commonPages.length ? commonPages.map((name) => formatPresence(name, signals.pagePresenceDetails[name])).join(" | ") : "none found"}`,
    `- Social links: ${
      signals.socialLinks.length
        ? signals.socialLinks.map((link) => `${link.label}: ${link.url}`).join(" | ")
        : "none found"
    }`
  ];
}

function renderSignalsHtml(result: ReviewResult): string {
  const signals = result.evidence.signals;
  if (!signals) {
    return `<p class="muted">Public detail extraction was not available for this review.</p>`;
  }

  const commonPages = (Object.entries(signals.pagePresence) as Array<[keyof typeof signals.pagePresence, boolean]>)
    .filter(([, present]) => present)
    .map(([name]) => name);

  return `<div class="detail-grid">
    ${renderDetail("Emails", signals.emails)}
    ${renderDetail("Phones", signals.phones)}
    ${renderDetail("Contact Forms", [`${countContactForms(result)} found`])}
    ${renderDetail("Address-Like Lines", signals.addressLines)}
    ${renderDetail("Common Pages", commonPages.map((name) => formatPresence(name, signals.pagePresenceDetails[name])))}
    ${renderDetail(
      "Social Links",
      signals.socialLinks.map((link) => `${link.label}: ${link.url}`)
    )}
  </div>`;
}

function renderDetail(label: string, values: string[]): string {
  return `<article class="detail">
    <strong>${escapeHtml(label)}</strong>
    ${
      values.length
        ? `<ul>${values.slice(0, 8).map((value) => `<li>${escapeHtml(value)}</li>`).join("")}</ul>`
        : `<p class="muted">None found</p>`
    }
  </article>`;
}

function countContactForms(result: ReviewResult): number {
  return result.evidence.pages.reduce((total, page) => total + (page.forms?.contactFormCount ?? 0), 0);
}

function formatPresence(name: string, detail?: { sources: string[]; urls: string[] }): string {
  if (!detail) {
    return name;
  }
  const sources = detail.sources.map((source) => source.replace(/_/g, " ")).join(", ");
  return `${name} (${sources || "found"})`;
}

function renderList(items: string[]): string {
  return `<ul>${items.map((item) => `<li>${escapeHtml(item)}</li>`).join("")}</ul>`;
}

function bulletList(items: string[]): string[] {
  return items.map((item) => `- ${item}`);
}

function formatConcern(concern: ReviewConcern): string {
  const page = concern.pageUrl ? ` (${concern.pageUrl})` : "";
  const evidence = concern.evidence ? ` Evidence: ${concern.evidence}` : "";
  const label = concern.label ? `${concern.label}: ` : "";
  return `[${concern.severity}] ${label}${concern.message}${page}.${evidence}`;
}

function severityRank(severity: ReviewConcern["severity"]): number {
  switch (severity) {
    case "high":
      return 3;
    case "medium":
      return 2;
    case "low":
      return 1;
  }
}

function groupConcerns(concerns: ReviewConcern[]): Array<[ConcernArea, ReviewConcern[]]> {
  const order: ConcernArea[] = ["page_quality", "content_quality", "business_identity", "policy_pages", "commercial_behavior"];
  return order
    .map((area): [ConcernArea, ReviewConcern[]] => [area, concerns.filter((concern) => concern.area === area)])
    .filter(([, groupedConcerns]) => groupedConcerns.length > 0);
}

function areaLabel(area: ConcernArea): string {
  switch (area) {
    case "page_quality":
      return "Page Loading And Links";
    case "content_quality":
      return "Content Quality";
    case "business_identity":
      return "Business Details";
    case "policy_pages":
      return "Common Policy Pages";
    case "commercial_behavior":
      return "Commercial Behavior";
  }
}

function readableHost(url: string): string {
  try {
    return new URL(url).hostname.replace(/^www\./, "");
  } catch {
    return url;
  }
}

function evidencePathBase(screenshotPath: string): string {
  return path.dirname(path.dirname(screenshotPath));
}

function escapeHtml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function escapeAttribute(value: string): string {
  return escapeHtml(value);
}
