export async function writeReports(result: ReviewResult, outputDir: string): Promise<ReportPaths> {
  await mkdir(outputDir, { recursive: true });

  const paths: ReportPaths = {
    outputDir,
    markdown: path.join(outputDir, "report.md"),
    html: path.join(outputDir, "report.html"),
    summaryHtml: path.join(outputDir, "summary.html"),
    summaryText: path.join(outputDir, "client-summary.txt"),
    checklistMarkdown: path.join(outputDir, "follow-up-checklist.md"),
    evidenceJson: path.join(outputDir, "evidence.json"),
    screenshotsDir: path.join(outputDir, "screenshots")
  };

  await Promise.all([
    writeFile(paths.markdown, renderMarkdown(result), "utf8"),
    writeFile(paths.html, renderFullHtml(result), "utf8"),
    writeFile(paths.summaryHtml, renderSummaryHtml(result), "utf8"),
    writeFile(paths.summaryText, renderPlainSummary(result), "utf8"),
    writeFile(paths.checklistMarkdown, renderChecklistMarkdown(result), "utf8"),
    writeFile(paths.evidenceJson, `${JSON.stringify(result, null, 2)}\n`, "utf8")
  ]);

  return paths;
}

function renderMarkdown(result: ReviewResult): string {
  const { evidence, concerns, summary } = result;
  const lines = [
    `# ${productName}`,
    "",
    `Website: ${evidence.input.websiteUrl}`,
    evidence.input.companyName ? `Company name provided: ${evidence.input.companyName}` : undefined,
    evidence.input.claimedLocation ? `Location provided: ${evidence.input.claimedLocation}` : undefined,
    evidence.input.expectedState ? `Expected state provided: ${evidence.input.expectedState}` : undefined,
    evidence.input.claimedIndustry ? `Industry/service provided: ${evidence.input.claimedIndustry}` : undefined,
    evidence.input.additionalIdentifiers ? `Additional identifiers provided: ${evidence.input.additionalIdentifiers}` : undefined,
    `Started: ${evidence.startedAt}`,
    `Pages reviewed: ${evidence.pages.length}`,
    `Review outcome: ${summary.riskLevel}`,
    `Concern level: ${summary.concernLevel}`,
    `Confidence: ${summary.confidence}`,
    `Likely official website: ${summary.likelyOfficialWebsite}`,
    "",
    "## Executive Summary",
    "",
    ...bulletList(executiveSummary(result)),
    "",
    "## Decision Brief",
    "",
    ...renderDecisionBriefMarkdown(result),
    "",
    "## Visible Conclusion",
    "",
    ...renderVisibleConclusionMarkdown(result),
    "",
    "## Verified Facts",
    "",
    ...bulletList(summary.verifiedFacts),
    "",
    "## Positive Indicators",
    "",
    ...bulletList(summary.positiveIndicators),
    "",
    "## Concerns And Indicators",
    "",
    ...bulletList(summary.concernIndicators.length ? summary.concernIndicators : ["No priority concern indicators were generated by the built-in checks."]),
    "",
    "## Unknown Or Unverified",
    "",
    ...bulletList(summary.unknowns.length ? summary.unknowns : ["No unknown items were generated by the built-in checks."]),
    "",
    "## Final Assessment",
    "",
    ...renderAssessmentMarkdown(result),
    "",
    "## Site Identity Summary",
    "",
    ...renderSiteIdentityMarkdown(result),
    "",
    "## Domain And Page Dates",
    "",
    ...renderDomainDatesMarkdown(result),
    "",
    "## External Evidence",
    "",
    ...renderExternalEvidenceMarkdown(result),
    "",
    "## Category Assessment",
    "",
    ...renderCategoryAssessmentMarkdown(result),
    "",
    "## Priority Follow-Up",
    "",
    ...renderPriorityMarkdown(result),
    "",
    "## Review Snapshot",
    "",
    ...renderSnapshotMarkdown(result),
    "",
    "## Verification Coverage",
    "",
    ...renderCoverageMarkdown(result),
    "",
    "## What Looked Complete",
    "",
    ...bulletList(summary.positiveNotes),
    "",
    "## Needs Follow-Up",
    "",
    ...bulletList(summary.followUpNotes),
    "",
    "## Suggested Next Steps",
    "",
    ...renderChecklistItems(result).map((item) => `- [ ] ${item}`),
    "",
    "## Public Details Found",
    "",
    ...renderSignalsMarkdown(result),
    "",
    "## Concerns By Category",
    ""
  ].filter((line): line is string => line !== undefined);

  if (concerns.length === 0) {
    lines.push("- No concerns were found by the built-in checks.", "");
  } else {
    for (const [area, groupedConcerns] of groupConcerns(concerns)) {
      lines.push(`### ${areaLabel(area)}`, "");
      for (const concern of groupedConcerns) {
        lines.push(`- ${formatConcern(concern)}`);
      }
      lines.push("");
    }
  }

  lines.push("## Pages Reviewed", "");
  for (const page of evidence.pages) {
    const pageEmails = page.details?.emails ?? [];
    const pagePhones = page.details?.phones ?? [];
    lines.push(`### ${page.title || page.finalUrl}`);
    lines.push("");
    lines.push(`- URL: ${page.finalUrl}`);
    lines.push(`- HTTP status: ${page.status ?? "not available"}`);
    lines.push(`- Meta description: ${page.metaDescription || "none found"}`);
    lines.push(`- Last modified: ${page.lastModified ? formatPageLastModified(page.lastModified) : "not available"}`);
    lines.push(`- Headings: ${page.headings?.length ? page.headings.join(" | ") : "none found"}`);
    lines.push(`- Links found: ${page.links.length}`);
    lines.push(`- Emails found: ${pageEmails.length}`);
    lines.push(`- Phones found: ${pagePhones.length}`);
    lines.push(`- Contact forms found: ${page.forms?.contactFormCount ?? 0}`);
    lines.push(`- Screenshot: ${path.relative(evidencePathBase(page.screenshotPath), page.screenshotPath) || page.screenshotPath}`);
    if (page.note) {
      lines.push(`- Note: ${page.note}`);
    }
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

